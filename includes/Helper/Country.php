	/**
	 * Get the ISO code by country name.
	 *
	 * @param string $country_name The full name of the country.
	 * @return string|null The two-letter ISO code or null if not found.
	 */
	public static function get_iso_code_by_name( string $country_name ): ?string {
		// Use array_flip to quickly search by value (country name) and return the key (code).
		$flipped = array_flip( self::get_countries() );
		// Use case-insensitive comparison.
		foreach ( $flipped as $name => $code ) {
			if ( strcasecmp( $country_name, $name ) === 0 ) {
				return $code;
			}
		}
		return null; // Return null if the country name is not found.
	}
